package secondTask.entity;
/**
 * here we  Make a program for finding the product of squares of the first two hundred numbers
 */
public class SquareCalculator {
    /**
     * that method to find Squares
     * @param length input number
     * @param width  input number
     * @param height input number
     * @return  length * width * height
     */
    public double findSquares(double length, double width, double height) {

        return length * width * height;

    }
}
